let currentLang = 'en';

function updateUIStrings() {
  const langData = translations[currentLang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const keys = el.getAttribute('data-i18n').split('.');
    let text = langData;
    for (const key of keys) {
      if (text && text[key]) {
        text = text[key];
      } else {
        text = null;
        break;
      }
    }
    
    if (text) {
      el.textContent = text;
    }
  });
}

document.addEventListener('DOMContentLoaded', () => {
    // Language Toggling Logic
    const langToggleBtn = document.getElementById('langToggle');
    if (langToggleBtn) {
        langToggleBtn.addEventListener('click', () => {
            if (currentLang === 'en') {
                currentLang = 'ar';
                document.documentElement.setAttribute('dir', 'rtl');
                document.documentElement.setAttribute('lang', 'ar');
                langToggleBtn.textContent = 'EN';
            } else {
                currentLang = 'en';
                document.documentElement.setAttribute('dir', 'ltr');
                document.documentElement.setAttribute('lang', 'en');
                langToggleBtn.textContent = 'AR';
            }
            updateUIStrings();
            
            // Re-update the segment indicator and FAQ heights
            const activeBtn = document.querySelector('.segment-btn.active');
            if (activeBtn) updateIndicatorPosition(activeBtn);

            setTimeout(() => {
                const activeItem = document.querySelector('.faq-item.active .faq-answer');
                if (activeItem) {
                    activeItem.style.maxHeight = activeItem.scrollHeight + 'px';
                }
            }, 100);
        });
    }

    // Initialize strings
    updateUIStrings();

    // Segmented Toggle Logic Setup
    const btnEmergency = document.getElementById('btn-emergency');
    const btnMaintenance = document.getElementById('btn-maintenance');
    const gridEmergency = document.getElementById('grid-emergency');
    const gridMaintenance = document.getElementById('grid-maintenance');
    const segmentIndicator = document.getElementById('segmentIndicator');

    function updateIndicatorPosition(activeBtn) {
        if (!segmentIndicator || !activeBtn) return;
        const isRtl = document.documentElement.getAttribute('dir') === 'rtl';
        
        segmentIndicator.style.width = activeBtn.offsetWidth + 'px';
        
        if (activeBtn.id === 'btn-emergency') {
            segmentIndicator.style.transform = 'translateX(0)';
        } else {
            const offset = btnEmergency.offsetWidth;
            if (isRtl) {
                segmentIndicator.style.transform = `translateX(-${offset}px)`;
            } else {
                segmentIndicator.style.transform = `translateX(${offset}px)`;
            }
        }
    }

    function switchTab(tab) {
        if (tab === 'emergency') {
            btnEmergency.classList.add('active');
            btnMaintenance.classList.remove('active');
            gridEmergency.style.display = 'grid';
            gridMaintenance.style.display = 'none';
            setTimeout(() => {
                gridEmergency.classList.remove('hidden-grid');
                gridEmergency.classList.add('active-grid');
                gridMaintenance.classList.remove('active-grid');
                gridMaintenance.classList.add('hidden-grid');
            }, 10);
            updateIndicatorPosition(btnEmergency);
        } else {
            btnMaintenance.classList.add('active');
            btnEmergency.classList.remove('active');
            gridMaintenance.style.display = 'grid';
            gridEmergency.style.display = 'none';
            setTimeout(() => {
                gridMaintenance.classList.remove('hidden-grid');
                gridMaintenance.classList.add('active-grid');
                gridEmergency.classList.remove('active-grid');
                gridEmergency.classList.add('hidden-grid');
            }, 10);
            updateIndicatorPosition(btnMaintenance);
        }
    }

    if (btnEmergency && btnMaintenance) {
        btnEmergency.addEventListener('click', () => switchTab('emergency'));
        btnMaintenance.addEventListener('click', () => switchTab('maintenance'));
        setTimeout(() => updateIndicatorPosition(btnEmergency), 100);
        window.addEventListener('resize', () => {
            const activeBtn = document.querySelector('.segment-btn.active');
            updateIndicatorPosition(activeBtn);
        });
    }

    // FAQ Accordion Logic
    const faqItems = document.querySelectorAll('.faq-item');
    faqItems.forEach(item => {
        const btn = item.querySelector('.faq-question');
        if (btn) {
            btn.addEventListener('click', () => {
                const isActive = item.classList.contains('active');
                
                // Auto-close others
                faqItems.forEach(otherItem => {
                    otherItem.classList.remove('active');
                    const otherAnswer = otherItem.querySelector('.faq-answer');
                    if (otherAnswer) otherAnswer.style.maxHeight = null;
                });
                
                if (!isActive) {
                    item.classList.add('active');
                    const answer = item.querySelector('.faq-answer');
                    if (answer) answer.style.maxHeight = answer.scrollHeight + 'px';
                }
            });
        }
    });
});
